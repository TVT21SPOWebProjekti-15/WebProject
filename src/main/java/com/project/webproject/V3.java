package com.project.webproject;

public class V3 {
    String Year;
    String Mean;

    public V3() {
    }

    public V3(String Year, String Mean) {
        this.Year = Year;
        this.Mean = Mean;
    }

    public String getYear() {
        return this.Year;
    }

    public void setYear(String Year) {
        this.Year = Year;
    }

    public String getMean() {
        return this.Mean;
    }

    public void setMean(String Mean) {
        this.Mean = Mean;
    }
}
